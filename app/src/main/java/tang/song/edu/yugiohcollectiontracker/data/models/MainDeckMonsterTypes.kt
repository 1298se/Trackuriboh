package tang.song.edu.yugiohcollectiontracker.data.models

enum class MainDeckMonsterTypes(val value: List<String>) {
    EFFECT_MONSTERS(
        listOf(
            BaseMonsterTypes.EFFECT_MONSTER.value,
            BaseMonsterTypes.FLIP_EFFECT_MONSTER.value
        )
    ),
    TUNER_MONSTERS(
        listOf(
            BaseMonsterTypes.TUNER_MONSTER.value,
            BaseMonsterTypes.PENDULUM_TUNER_EFFECT_MONSTER.value,
            BaseMonsterTypes.NORMAL_TUNER_MONSTER.value
        )
    ),
    FLIP_MONSTERS(
        listOf(
            BaseMonsterTypes.FLIP_EFFECT_MONSTER.value,
            BaseMonsterTypes.PENDULUM_FLIP_EFFECT_MONSTER.value
        )
    ),
    UNION_MONSTERS(
        listOf(
            BaseMonsterTypes.UNION_EFFECT_MONSTER.value
        )
    ),
    PENDULUM_MONSTERS(
        listOf(
            BaseMonsterTypes.PENDULUM_EFFECT_MONSTER.value,
            BaseMonsterTypes.PENDULUM_FLIP_EFFECT_MONSTER.value,
            BaseMonsterTypes.PENDULUM_NORMAL_MONSTER.value,
            BaseMonsterTypes.PENDULUM_TUNER_EFFECT_MONSTER.value
        )
    ),
    RITUAL_MONSTERS(
        listOf(
            BaseMonsterTypes.RITUAL_EFFECT_MONSTER.value,
            BaseMonsterTypes.RITUAL_MONSTER.value
        )
    ),
    MAIN_DECK_MONSTERS(
        EFFECT_MONSTERS.value
            .union(TUNER_MONSTERS.value)
            .union(FLIP_MONSTERS.value)
            .union(UNION_MONSTERS.value)
            .union(PENDULUM_MONSTERS.value)
            .union(RITUAL_MONSTERS.value).toList()
    ),
}
